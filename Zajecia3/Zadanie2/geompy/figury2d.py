import math

def pole_kwadratu(bok):
    return bok ** 2

def obwod_kwadratu(bok):
    return 4 * bok

def pole_prostokata(a, b):
    return a * b

def obwod_prostokata(a, b):
    return 2 * (a + b)

def pole_kola(promien):
    return math.pi * promien ** 2

def obwod_kola(promien):
    return 2 * math.pi * promien
